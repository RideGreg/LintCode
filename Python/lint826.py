class Solution:
    """
    @param n: the rows of matrix
    @param m: the cols of matrix
    @param Badcomputers: the bad computers
    @return: The answer
    """
    def maintenance(self, n, m, Badcomputers):
        most = [[m-1, 0] for _ in xrange(n)]
        for r,c in Badcomputers:
            most[r][0] = min(most[r][0], c)
            most[r][1] = max(most[r][1], c)
        lDist, rDist = -1, float('inf')
        for lMost, rMost in most:
            lDist, rDist = min(lDist+rMost*2, rDist+m-1) + 1, min(lDist+m-1, rDist+(m-1-lMost)*2) + 1
        return rDist

